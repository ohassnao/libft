#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		index;
	char	*dest;
	char	*tab;

	index = 0;
	tab = ((dest = (char *)malloc(ft_strlen(src) * sizeof(char) + 1)));
	if (!tab)
	{
		return (0);
	}
	while (src[index])
	{
		dest[index] = src[index];
		index++;
	}
	dest[index] = '\0';
	return (dest);
}
